def invalidate_downstream_steps(task: TaskState, target_step_id: str):
    """
    Recursively invalidates all downstream steps depending on the target_step_id.
    Resets plan step status to 'pending', clears downstream artifacts and StepResults,
    and increments the retry count on the target step.
    """
    # Create steps lookup map
    steps_map = {step.step_id: step for step in task.plan.steps}
    
    # BFS to find transitive dependency closure of steps to invalidate
    invalidated = {target_step_id}
    added = True
    while added:
        added = False
        for step in task.plan.steps:
            if step.step_id not in invalidated:
                # If this step depends on any invalidated step, it must be invalidated
                if any(dep in invalidated for dep in step.depends_on):
                    invalidated.add(step.step_id)
                    added = True
                    
    logger.info(f"Invalidating steps due to retry of '{target_step_id}': {invalidated}")
    
    # Reset states for all invalidated steps
    for step_id in invalidated:
        if step_id in steps_map:
            steps_map[step_id].status = "pending"
            
        if step_id in task.step_results:
            res = task.step_results[step_id]
            res.status = "pending"
            res.output_artifact_ids = []
            res.error = None
            res.completed_at = None
            if step_id == target_step_id:
                res.retry_count += 1
            else:
                res.retry_count = 0
        else:
            # Create a pending StepResult if none exists
            task.step_results[step_id] = StepResult(
                step_id=step_id,
                status="pending",
                retry_count=1 if step_id == target_step_id else 0
            )
                
    # Remove artifacts generated by any of the invalidated steps
    artifacts_to_keep = []
    for art in task.artifacts:
        if art.source_step_id in invalidated:
            try:
                local_path = task_store.workspace_root.joinpath(art.path_or_url)
                if local_path.exists() and local_path.is_file():
                    os.remove(local_path)
                    logger.info(f"Deleted invalidated artifact file: {local_path}")
            except Exception as e:
                logger.warning(f"Failed to delete invalidated artifact file {art.path_or_url}: {e}")
        else:
            artifacts_to_keep.append(art)
    task.artifacts = artifacts_to_keep


async def run_execution_graph(task_id: str):
    """Background task runner to execute the sequential graph and handle top level exceptions."""
    try:
        task = task_store.load_task(task_id)
        if not task:
            logger.error(f"Task {task_id} not found for background execution.")
            return
        
        if task.status != "executing":
            logger.warning(f"Task {task_id} status is '{task.status}', not 'executing'. Aborting background run.")
            return
            
        await company_execution_graph.ainvoke({"task_id": task_id})
    except Exception as e:
        logger.exception(f"Exception in background run for task {task_id}")
        task = task_store.load_task(task_id)
        if task:
            task.status = "failed"
            task.error = StepError(
                error_type="worker_error",
                message=f"Background execution exception: {str(e)}",
                details={}
            )
            task.completed_at = datetime.now().isoformat()
            task.updated_at = datetime.now().isoformat()
            task_store.save_task(task)


@router.post("", response_model=dict)
async def create_task(req: CreateTaskRequest):
    """
    Creates a new multi-agent consulting task.
    Invokes the Supervisor planning node synchronously to construct the JSON execution plan.
    """
    task_id = f"task_{int(datetime.now().timestamp())}_{uuid.uuid4().hex[:6]}"
    now_iso = datetime.now().isoformat()
    
    task = TaskState(
        task_id=task_id,
        user_query=req.user_query,
        status="planning",
        created_at=now_iso,
        updated_at=now_iso
    )
    task_store.save_task(task)
    
    try:
        await company_graph.ainvoke({"task_id": task_id})
    except Exception as e:
        logger.exception(f"Planning failed during initial creation of task {task_id}")
        # Mark as planning failed
        task = task_store.load_task(task_id)
        if task and task.status == "planning":
            task.status = "planning_failed"
            task.error = StepError(
                error_type="validation_error",
                message=f"Plan generation failed with error: {str(e)}",
                details={}
            )
            task.updated_at = datetime.now().isoformat()
            task_store.save_task(task)
            
    updated_task = task_store.load_task(task_id)
    if not updated_task:
        raise HTTPException(status_code=500, detail="Task state could not be reloaded.")
        
    return {
        "success": True,
        "data": updated_task,
        "error": None
    }


