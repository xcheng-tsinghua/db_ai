@router.put("/{task_id}/plan", response_model=dict)
async def update_plan(task_id: str, plan: ExecutionPlan):
    """Allows updating/modifying the step configurations prior to starting execution."""
    task = task_store.load_task(task_id)
    if not task:
        raise HTTPException(status_code=404, detail=f"Task '{task_id}' not found")
    if task.status not in ["pending_approval", "planning_failed"]:
        raise HTTPException(
            status_code=400,
            detail=f"Cannot update plan for task in status '{task.status}'. Plan is already finalized or running."
        )
        
    task.plan = plan
    task.status = "pending_approval"  # Reset status if it was planning_failed
    task.updated_at = datetime.now().isoformat()
    task_store.save_task(task)
    
    return {
        "success": True,
        "data": task,
        "error": None
    }


@router.post("/{task_id}/confirm", response_model=dict)
async def confirm_task(task_id: str, background_tasks: BackgroundTasks):
    """Approves the plan and starts sequential worker execution asynchronously."""
    task = task_store.load_task(task_id)
    if not task:
        raise HTTPException(status_code=404, detail=f"Task '{task_id}' not found")
    if task.status not in ["pending_approval", "failed", "cancelled"]:
        raise HTTPException(
            status_code=400,
            detail=f"Task is in '{task.status}', cannot transition to execution. Require 'pending_approval' or failed/cancelled state."
        )
        
    now_iso = datetime.now().isoformat()
    task.status = "executing"
    task.approved_at = now_iso
    task.updated_at = now_iso
    task.error = None
    
    # Initialize/Reset step results and plan step statuses
    for step in task.plan.steps:
        if step.step_id not in task.step_results:
            task.step_results[step.step_id] = StepResult(
                step_id=step.step_id,
                status="pending"
            )
        else:
            res = task.step_results[step.step_id]
            if res.status in ["failed", "skipped"]:
                res.status = "pending"
                res.error = None
                step.status = "pending"
                
    task_store.save_task(task)
    background_tasks.add_task(run_execution_graph, task_id)
    
    return {
        "success": True,
        "data": task,
        "error": None
    }


@router.post("/{task_id}/cancel", response_model=dict)
async def cancel_task(task_id: str):
    """Cancels a running execution, stopping at the next step boundary."""
    task = task_store.load_task(task_id)
    if not task:
        raise HTTPException(status_code=404, detail=f"Task '{task_id}' not found")
    if task.status != "executing":
        raise HTTPException(status_code=400, detail=f"Cannot cancel task in '{task.status}' state.")
        
    now_iso = datetime.now().isoformat()
    task.status = "cancelled"
    task.error = StepError(
        error_type="cancelled",
        message="Task execution was cancelled by the user.",
        details={}
    )
    task.completed_at = now_iso
    task.updated_at = now_iso
    
    # Set pending/running steps to cancelled/failed
    for step in task.plan.steps:
        if step.status in ["running", "pending"]:
            step.status = "cancelled"
        if step.step_id in task.step_results:
            res = task.step_results[step.step_id]
            if res.status in ["running", "pending"]:
                res.status = "failed"
                res.error = StepError(
                    error_type="cancelled",
                    message="Cancelled by user action.",
                    details={}
                )
                res.completed_at = now_iso
                
    task_store.save_task(task)
    
    return {
        "success": True,
        "data": task,
        "error": None
    }


@router.post("/{task_id}/steps/{step_id}/retry", response_model=dict)
async def retry_step(task_id: str, step_id: str, background_tasks: BackgroundTasks):
    """
    Performs downstream step invalidation, increments the step retry count,
    resets step and task statuses, and resumes execution in the background.
    """
    task = task_store.load_task(task_id)
    if not task:
        raise HTTPException(status_code=404, detail=f"Task '{task_id}' not found")
    if task.status not in ["completed", "failed", "cancelled", "executing"]:
        raise HTTPException(
            status_code=400,
            detail=f"Cannot retry step for task in '{task.status}' state."
        )
        
    # Check that step_id exists
    step_exists = any(step.step_id == step_id for step in task.plan.steps)
    if not step_exists:
        raise HTTPException(status_code=404, detail=f"Step '{step_id}' not found in execution plan.")
        
    # Apply invalidation
    invalidate_downstream_steps(task, step_id)
    
    # Clear top-level errors and final outcomes
    task.final_summary = None
    task.error = None
    task.status = "executing"
    task.updated_at = datetime.now().isoformat()
    
    task_store.save_task(task)
    background_tasks.add_task(run_execution_graph, task_id)
    
    return {
        "success": True,
        "data": task,
        "error": None
    }
